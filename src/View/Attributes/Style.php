  public function string()
  {
    $string = '';
    $styleProperties = Properties::cases();
    $styles = get_object_vars($this);

    foreach ($styleProperties as $property) :
      $key = $property->name;
      $name = $property->value;

      if ($option = $styles[$key] ?? null) :
        $string.= "{$name}:{$option->value};";
      endif;
    endforeach;

    return $string;
  }
